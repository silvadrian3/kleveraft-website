    function submit_message(){
        var firstname = $("#firstname"),
            lastname = $("#lastname"),
            brandname = $("#brandname"),
            email = $("#email"),
            contactno = $("#contactno"),
            message = $("#message");

        if(firstname.val() === "" || firstname.val === undefined){
            alert("First Name is required");
            firstname.focus();
            firstname.addClass("alert-danger");
            return false;
        } else if(lastname.val() === "" || lastname.val === undefined){
            alert("Last Name is required");
            lastname.focus();
            lastname.addClass("alert-danger");
            return false;
        } else if(brandname.val() === "" || brandname.val === undefined){
            alert("Brand Name is required");
            brandname.focus();
            brandname.addClass("alert-danger");
            return false;
        } else if(email.val() === "" || email.val === undefined){
            alert("Email Address is required");
            email.focus();
            email.addClass("alert-danger");
            return false;
        } else if(contactno.val() === "" || contactno.val === undefined){
            alert("Contact No. is required");
            contactno.focus();
            contactno.addClass("alert-danger");
            return false;
        } else {
            return true;
        }

    }
        
